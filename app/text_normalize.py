_PHRASE_REPLACEMENTS = {
    '手機': '手机',
    '眼鏡': '眼镜',
    '鑰匙': '钥匙',
    '遙控器': '遥控器',
    '電腦': '电脑',
    '電話': '电话',
    '書本': '书本',
    '手錶': '手表',
    '蘋果': '苹果',
}


def to_simplified(text):
    result = str(text or '')
    for source, target in _PHRASE_REPLACEMENTS.items():
        result = result.replace(source, target)
    return result.translate(_TRADITIONAL_TO_SIMPLIFIED)
